#logical operators
# and
# or
# not

# and is price between 10 and 30
price = 5
print(price > 10 and price < 30) #true

# or is price either less than 10 or greater than 30
price = 25
print(price > 10 or price < 30) #true

# not is price not between 10 and 30
price = 5
print(not price > 10) #true



