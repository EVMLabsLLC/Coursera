#indexing strings
course = "Python for Beginners"
print (len(course))
print(course[0]) #first character
print(course[-1]) #last character
print(course[0:3]) #first 3 characters
print(course[0:]) #all characters
print(course[:3]) #first 3 characters
print(course[:]) #all characters

#methods
print(course.replace("for Beginners", "for Experts"))
